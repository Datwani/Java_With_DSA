public class Q1_PositiveorNegative {
    public static void main(String[] args) {
        int num=24;

        if (num==0)
        {
            System.out.println("Number is 0");
        }else {
            if (num<0)
            {
                System.out.println("Negative number");
            }
            else
            {
                System.out.println("Positive number");
            }
        }
    }
}
